/**
 * ====================================
 * Do while loop
 * ====================================
 */

// no matter what, code will execute whether the condition is true or false. The code will run for at least one.

var i = 11;
var n = 10;

do {
  console.log(i);
  i++;
} while (i <= n);

let x = 0;

do {
  console.log(`Value of x: ${x}`);
  x++;
} while (x < 3);
